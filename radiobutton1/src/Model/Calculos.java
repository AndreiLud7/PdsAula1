package Model;

public class Calculos {

	public Integer adição (int primeiro, int segundo) {
		
		int resultado;
		resultado = primeiro + segundo;		
		return resultado;
	}
	public Integer subtracao (int primeiro, int segundo) {
		int resultado;
		resultado = primeiro - segundo;
		return resultado;
	}
	public Integer multiplicacao(int primeiro, int segundo) {
		int resultado;
		resultado = primeiro * segundo;
		return resultado;
	}
	public Integer divisao(int primeiro, int segundo) {
		int resultado;


		resultado = primeiro / segundo;
		return resultado;
	}
	
	
}
